#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#define ll long long
#define MAX 100
struct Solieu{
    char tenhang[50];
    float dongia, thanhtien;
    ll soluong, STT;
};
typedef struct Solieu sl;
void swap(sl *a, sl *b)
{
    sl temp;
    temp = *a;
    *a = *b;
    *b = temp;
}
void sapxep(sl ds[], int n)
{
    for(int i = 0; i < n - 1; i++)
    {
        for(int j = 0; j < n - i - 1; j++)
            if(strcmp(ds[j].tenhang, ds[j + 1].tenhang) > 0)
                swap(&ds[j], &ds[j + 1]);
    }
}
int main()
{
    //FILE* f;
    //f = fopen("testfileinput.txt", "r");
    //f = fopen("testfileoutput.txt", "w");
    int n;
    sl ds[MAX];
    float tongtien = 0;
    printf("Nhap so luong hang hoa: ");
    scanf("%d",&n);
    for(int i = 0; i < n; i++)
    {
        printf("Nhap luong hang thu %d \n",i + 1);
        ds[i].STT = i + 1;
        printf("Nhap ten hang: ");
        scanf("%s", ds[i].tenhang);
        printf("Nhap don gia: ");
        scanf("%f", &ds[i].dongia);
        printf("Nhap so luong: ");
        scanf("%lld", &ds[i].soluong);
        ds[i].thanhtien = ds[i].dongia * ds[i].soluong;
        tongtien += ds[i].thanhtien;
    }
    sapxep(ds, n);
    printf("            SO LIEU BAN HANG        \n");
    printf("%-5s %-10s %-10s %-10s %-10s\n","STT", "Ten Hang", "Don gia", "So luong", "Thanh tien");
    for(int i = 0; i < n ; i++)
    {
        printf("%-5lld %-10s %-10.2f %-10lld %-10.2f", ds[i].STT, ds[i].tenhang, ds[i].dongia, ds[i].soluong, ds[i].thanhtien);
        printf("\n");
    }
    printf("%38s","Tong tien ");
    printf(" %.2f ", tongtien);
    //fclose(f);
    return 0;
}
