#include <stdlib.h>
#include <math.h>
#define ll long long

int main()
{
    int n, max, min, vtrimax, vtrimin;
    printf("Nhap vao so phan tu cua mang: ");
    scanf("%d", &n);
    int a[n];
    printf("Nhap cac phan tu cua mang: ");
    for(int i = 0; i < n; i++)
        scanf("%d", &a[i]);
    for(int i = 0; i < n; i++)
    {
        max = a[0];
        if(a[i] > max)
        {
            max = a[i];
            vtrimax = i;
        }
    }
    for(int i = 0; i < n; i++)
    {
        min = a[0];
        if(a[i] <= min)
        {
            min = a[i];
            vtrimin = i;
        }
    }
    printf("Phan tu lon nhat trong mang va vi tri la %d %d ", max, vtrimax);
    printf("\n");
    printf("Phan tu nho nhat trong mang va vi tri la %d %d ", min, vtrimin);
    return 0;
}
